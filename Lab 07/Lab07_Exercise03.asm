.ORG 0x0
	JMP MAIN

.ORG 0x08
	JMP PCINT8_ISR

.ORG 0x100
MAIN:	LDI R16, HIGH(RAMEND)
	OUT SPH, R16
	LDI R16, LOW(RAMEND)
	OUT SPL, R16

	CALL PIN_SETUP
	LDI R16, (1<<PCIE1)
	STS PCICR, R16
	LDI R16, 0x01
	STS PCMSK1, R16
	SEI
	LDI R20, 2

LOOP:	SBIC PIND, 4
	RJMP L_OFF
	RJMP L_ON

L_OFF:	CBI PORTB, 0
	RJMP LOOP

L_ON:	SBI PORTB, 0
	RJMP LOOP

PIN_SETUP:
	SBI DDRB, 0
	CBI PORTB, 0
	SBI DDRB, 1
	CBI PORTB, 1
	CBI DDRD, 4
	SBI PORTD, 4
	SBI PORTC, 0
	RET

.ORG 0x200
PCINT8_ISR:
	DEC R20
	BRNE HERE
	IN R17, PORTB
	LDI R18, (1<<1)
	EOR R17, R18
	OUT PORTB, R17
	LDI R20, 2
HERE:	RETI